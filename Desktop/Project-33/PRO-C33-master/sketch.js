function draw() {
  background("black");
  
  textSize(20)
  text("Score : "+score,20,30);

  Engine.update(engine);
  
  ground.display();
  
   for (var i = 0; i < plinkos.length; i++) {
     
     plinkos[i].display();
     
   }
   if(frameCount%100===0){
     particles.push(new Particle(random(mouseX, width/2+30), 10,10));
     score++;
   }
 
  for (var j = 0; j < particles.length; j++) {
   
     particles[j].display();
   }
   for (var k = 0; k < divisions.length; k++) {
     
     divisions[k].display();
   }
}

